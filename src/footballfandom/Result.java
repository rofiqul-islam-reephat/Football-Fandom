    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String tournament = (String) tournamentCombo.getSelectedItem();
  
        
        try {  
             Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");  
             Connection connection = DriverManager.getConnection("jdbc:sqlserver://localhost:1433;user=user;password=SILVER;" +
            "databaseName=footballFandomdb;");  
   
             Statement statement = connection.createStatement();  
             
             ResultSet rs;
             
             if(tournament.equals("All Tournament"))
             {
               rs = statement.executeQuery(
            "select CONVERT(VARCHAR(24),Schedule.[s_Date], 113)  as 'Date',(select Team.T_name from Team where T_id = Match.Home_T_id) as HomeTeam,(select Team.T_name from Team where T_id = Match.Away_T_id) as AwayTeam,Home_T_Goal,Away_Team_Goal from Match\n" +
            "right join Match_Result on Match_Result.M_id = Match.M_id\n" +
            "left join Schedule on Match.M_id = Schedule.M_id\n" +
            "order by Date asc");  
             }
             else
             {
                rs = statement.executeQuery(
            "select CONVERT(VARCHAR(24),Schedule.[s_Date], 113)  as 'Date',(select Team.T_name from Team where T_id = Match.Home_T_id) as HomeTeam,(select Team.T_name from Team where T_id = Match.Away_T_id) as AwayTeam,Home_T_Goal,Away_Team_Goal from Match\n" +
            "right join Match_Result on Match_Result.M_id = Match.M_id\n" +
            "left join Schedule on Match.M_id = Schedule.M_id\n" +
            "where Match.Tournament_id = (select Tournament_id from Tournament where Tournament_name = '"+tournament+"')\n" +
            "order by Date asc");  
             
             }
             
             ResultSetMetaData  rsmetadata = rs.getMetaData(); 
              int  columns = rsmetadata.getColumnCount();   
              DefaultTableModel dtm = new DefaultTableModel(); 
              Vector columns_name=new Vector();
              Vector data_rows=new Vector();
              for(int  i= 1; i < columns+1; i++)  
                {             
                        columns_name.addElement (rsmetadata.getColumnLabel(i));  
                }                 
              
              dtm.setColumnIdentifiers(columns_name);
                   while (rs.next())  
           {  
               data_rows = new Vector(); 
               for (int j = 1; j <columns+1; j++)  
               {  
                   data_rows.addElement(rs.getString(j)) ;
               }  
               dtm.addRow(data_rows);  
               
       }   
          jTable1.setModel(dtm); 
             
         } catch (Exception e) {  
             e.printStackTrace();  
         }  
    }//GEN-LAST:event_jButton1ActionPerformed

